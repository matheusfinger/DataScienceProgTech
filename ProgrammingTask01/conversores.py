def celsius_para_fahrenheit(temp):
    return (temp * (9/5)) + 32

def fahrenheit_para_celsius(temp):
    return (temp - 32) * (5/9)

def pes_para_metro(pes):
    return pes / 3.281

def quilometro_para_milhas(distancia):
    return distancia / 1.609

def milhas_para_quilometro(distancia):
    return distancia * 1.609

def dia_para_horas(dias):
    return dias * 24